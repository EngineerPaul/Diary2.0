#!/bin/bash

# Entrypoint —Å–∫—Ä–∏–ø—Ç –¥–ª—è Certbot –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞
# –í—ã–ø–æ–ª–Ω—è–µ—Ç –ø–æ–ª—É—á–µ–Ω–∏–µ/–æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–æ–≤
# —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –æ—Å—É—â–µ—Å—Ç–≤–ª—è–µ—Ç—Å—è –ø–æ –ø—É—Ç–∏ /etc/nginx/ssl-output –≤–Ω—É—Ç—Ä–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞,
# –æ—Ç–∫—É–¥–∞ –ø—Ä–∏ –ø–æ–º–æ—â–∏ —Ç–æ–º–∞ –æ–Ω–∏ –ø–æ–ø–∞–¥–∞—é—Ç –≤ –ø–∞–ø–∫—É ../nginx/ssl –Ω–∞ —Ö–æ—Å—Ç–µ

set -e  # –í—ã—Ö–æ–¥ –ø—Ä–∏ –ª—é–±–æ–π –æ—à–∏–±–∫–µ

echo "=== Certbot SSL Factory ==="
echo "–î–æ–º–µ–Ω: ${DOMAIN}"
echo "Email: ${EMAIL}"
echo "–†–µ–∂–∏–º: ${CERTBOT_MODE:-staging}"

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã—Ö –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö
if [ -z "$DOMAIN" ] || [ -z "$EMAIL" ]; then
    echo "‚ùå –û—à–∏–±–∫–∞: DOMAIN –∏ EMAIL –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —É–∫–∞–∑–∞–Ω—ã –≤ .env —Ñ–∞–π–ª–µ"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É—é—Ç –ª–∏ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã –¥–ª—è —ç—Ç–æ–≥–æ –¥–æ–º–µ–Ω–∞
if [ ! -d "/etc/letsencrypt/live/${DOMAIN}" ]; then
    echo "üîë –ü–æ–ª—É—á–∞–µ–º –Ω–æ–≤—ã–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã –¥–ª—è –¥–æ–º–µ–Ω–∞: ${DOMAIN}"
    
    # –í—ã–±–∏—Ä–∞–µ–º —Ä–µ–∂–∏–º —Ä–∞–±–æ—Ç—ã certbot –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
    if [ "$CERTBOT_MODE" = "production" ]; then
        echo "üöÄ –ò—Å–ø–æ–ª—å–∑—É–µ–º PRODUCTION —Ä–µ–∂–∏–º (—Ä–µ–∞–ª—å–Ω—ã–µ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã)"
        CERTBOT_ARGS=""
    else
        echo "üß™ –ò—Å–ø–æ–ª—å–∑—É–µ–º STAGING —Ä–µ–∂–∏–º (—Ç–µ—Å—Ç–æ–≤—ã–µ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã)"
        CERTBOT_ARGS="--staging"
    fi
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å –¥–æ–º–µ–Ω–∞ –ø–µ—Ä–µ–¥ –∑–∞–ø—Ä–æ—Å–æ–º —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–æ–≤
    echo "üîç –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å –¥–æ–º–µ–Ω–∞..."
    if curl -f --connect-timeout 10 "http://${DOMAIN}" > /dev/null 2>&1; then
        echo "‚úÖ –î–æ–º–µ–Ω –¥–æ—Å—Ç—É–ø–µ–Ω –ø–æ HTTP"
    else
        echo "‚ùå –û—à–∏–±–∫–∞: –î–æ–º–µ–Ω ${DOMAIN} –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω –ø–æ HTTP"
        echo "–£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ:"
        echo "1. –î–æ–º–µ–Ω —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞ IP –∞–¥—Ä–µ—Å —Å–µ—Ä–≤–µ—Ä–∞"
        echo "2. –ü–æ—Ä—Ç 80 –æ—Ç–∫—Ä—ã—Ç –∏ –¥–æ—Å—Ç—É–ø–µ–Ω"
        echo "3. Nginx –≤—Ä–µ–º–µ–Ω–Ω—ã–π –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –∑–∞–ø—É—â–µ–Ω"
        exit 1
    fi
    
    # –ü–æ–ª—É—á–∞–µ–º —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º webroot –º–µ—Ç–æ–¥–∞
    # webroot-path - –ø–∞–ø–∫–∞ –≥–¥–µ Let's Encrypt –∏—â–µ—Ç —Ñ–∞–π–ª—ã –≤–µ—Ä–∏—Ñ–∏–∫–∞—Ü–∏–∏
    certbot certonly \
        --webroot \
        --webroot-path=/var/www/certbot \
        --email ${EMAIL} \
        --agree-tos \
        --no-eff-email \
        ${CERTBOT_ARGS} \
        -d ${DOMAIN}
    
    echo "‚úÖ –°–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã —É—Å–ø–µ—à–Ω–æ –ø–æ–ª—É—á–µ–Ω—ã!"
    
else
    echo "üîÑ –°–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É—é—Ç, –ø—Ä–æ–≤–µ—Ä—è–µ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç—å –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è"
    
    # –û–±–Ω–æ–≤–ª—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã
    # Certbot —Å–∞–º –ø—Ä–æ–≤–µ—Ä–∏—Ç —Å—Ä–æ–∫ –¥–µ–π—Å—Ç–≤–∏—è –∏ –æ–±–Ω–æ–≤–∏—Ç –µ—Å–ª–∏ –Ω—É–∂–Ω–æ
    certbot renew
    
    echo "‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –∑–∞–≤–µ—Ä—à–µ–Ω–∞"
fi

# –ö–æ–ø–∏—Ä—É–µ–º —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã –≤ –≤—ã—Ö–æ–¥–Ω—É—é –ø–∞–ø–∫—É –¥–ª—è nginx
# –≠—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –æ—Å–Ω–æ–≤–Ω–æ–º—É –ø—Ä–æ–µ–∫—Ç—É –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –≥–æ—Ç–æ–≤—ã–µ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã
echo "üìã –ö–æ–ø–∏—Ä—É–µ–º —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã –≤ –≤—ã—Ö–æ–¥–Ω—É—é –ø–∞–ø–∫—É..."

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–æ–≤ –ø–µ—Ä–µ–¥ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ–º
if [ ! -f "/etc/letsencrypt/live/${DOMAIN}/fullchain.pem" ] || [ ! -f "/etc/letsencrypt/live/${DOMAIN}/privkey.pem" ]; then
    echo "‚ùå –û—à–∏–±–∫–∞: –°–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ /etc/letsencrypt/live/${DOMAIN}/"
    exit 1
fi

# –ö–æ–ø–∏—Ä—É–µ–º –ø–æ–ª–Ω—É—é —Ü–µ–ø–æ—á–∫—É –∏ –ø—Ä–∏–≤–∞—Ç–Ω—ã–π –∫–ª—é—á –≤ –ø—É—Ç—å, –∫–æ—Ç–æ—Ä—ã–π –∏—Å–ø–æ–ª—å–∑—É–µ—Ç —Ç–æ–º docker (../nginx/ssl:/etc/nginx/ssl-output)
cp /etc/letsencrypt/live/${DOMAIN}/fullchain.pem /etc/nginx/ssl-output/cert.pem
cp /etc/letsencrypt/live/${DOMAIN}/privkey.pem /etc/nginx/ssl-output/key.pem

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ –ø—Ä–∞–≤–∞ –¥–æ—Å—Ç—É–ø–∞ –≤–Ω—É—Ç—Ä–∏ —Ç–æ–º–∞, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ—Ç–æ–º —Ä–∞—Å–ø—Ä–æ—Å—Ç—Ä–∞–Ω—è—Ç—Å—è –Ω–∞ nginx –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
# 644 - —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç –º–æ–∂–µ—Ç —á–∏—Ç–∞—Ç—å –∫—Ç–æ —É–≥–æ–¥–Ω–æ
# 600 - –ø—Ä–∏–≤–∞—Ç–Ω—ã–π –∫–ª—é—á –º–æ–∂–µ—Ç —á–∏—Ç–∞—Ç—å —Ç–æ–ª—å–∫–æ root
chmod 644 /etc/nginx/ssl-output/cert.pem
chmod 600 /etc/nginx/ssl-output/key.pem

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è
if [ -f "/etc/nginx/ssl-output/cert.pem" ] && [ -f "/etc/nginx/ssl-output/key.pem" ]; then
    echo "‚úÖ –°–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã —É—Å–ø–µ—à–Ω–æ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã –≤ /etc/nginx/ssl-output/"
    
    # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞—Ö - —á–∏—Ç–∞–µ–º –Ω–µ–∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–µ –º–µ—Ç–∞–¥–∞–Ω–Ω—ã–µ
    # -noout -dates - –≤—ã–≤–µ–¥–µ—Ç –¥–∞—Ç—ã –Ω–∞—á–∞–ª–∞ –∏ –æ–∫–æ–Ω—á–∞–Ω–∏—è –¥–µ–π—Å—Ç–≤–∏—è —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞
    echo "üìÑ –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞—Ö:"
    openssl x509 -in /etc/nginx/ssl-output/cert.pem -noout -dates
    echo ""
    
else
    echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–æ–ø–∏—Ä–æ–≤–∞—Ç—å —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç—ã"
    exit 1
fi

echo "üéâ SSL Factory —É—Å–ø–µ—à–Ω–æ –∑–∞–≤–µ—Ä—à–∏–ª–∞ —Ä–∞–±–æ—Ç—É!"
